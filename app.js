function runCounter() {
    jQuery(document).ready(function ($) {
        $(".counter").counterUp({
            delay: 10,
            time: 1000,
        });
    });
    // get the select option;
    const province = document.getElementById("province");

    // select all data which is selected
    const seletedData = rates.ours.filter((item) => item.province === province.value)[0];

    // create an array and put all id name in here
    const allId = ['fixed1Yr', 'fixed3Yr', 'fixed5Yr', 'fixed7Yr', 'variable1Yr']

    // loop througt in all id to get id element and pass for matches in matchRates function
    allId.forEach((item) => {
        let selectId = document.getElementById(item);
        matchRates(selectId)
    })

    // update all data which matches with keys;
    function matchRates(selectId) {
        for(const [key, value] of Object.entries(seletedData)){
             if(selectId.id == key){
                selectId.innerHTML = value.toFixed(2) ?? 0;
             }
        }
    }
}

